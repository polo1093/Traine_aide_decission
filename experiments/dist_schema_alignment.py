def run_alignment(
    *,
    dist_dir: str | Path = DEFAULT_DIST_DIR,
    current_csv: str | Path = DEFAULT_CURRENT_CSV,
    v5_output: str | Path = DEFAULT_V5_CSV,
    report_output: str | Path = DEFAULT_REPORT,
) -> dict[str, Any]:
    dist_path = Path(dist_dir)
    current_path = Path(current_csv)
    v5_path = Path(v5_output)
    report_path = Path(report_output)

    references = load_dist_references(dist_path)
    dist_records = [record for _, record in references]
    dist_schema = infer_json_schema(dist_records)
    dist_flat_columns = sorted(flatten_schema_paths(dist_schema))

    export_summary = export_dist_aligned_candidate_csv(
        current_path,
        v5_path,
        fieldnames=dist_flat_columns,
    )

    current_rows, current_fieldnames = load_csv_rows(current_path)
    v5_rows, v5_fieldnames = load_csv_rows(v5_path)
    current_schema = infer_csv_schema(current_rows, current_fieldnames)
    v5_schema = infer_csv_schema(v5_rows, v5_fieldnames)

    missing_current = sorted(field for field in dist_flat_columns if field not in current_fieldnames)
    missing_v5 = sorted(field for field in dist_flat_columns if field not in v5_fieldnames)
    ignored_by_model = sorted(
        field for field in v5_fieldnames
        if field not in FEATURE_COLUMNS and field not in {"bootstrap_label", "excluded"}
    )
    leakage_columns = leakage_risk_columns(v5_fieldnames)
    leakage_used = sorted(field for field in leakage_columns if field in FEATURE_COLUMNS)

    report = {
        "status": "ok",
        "dist_reference_files": sorted({str(path) for path, _ in references}),
        "dist_reference_schema": dist_schema,
        "dist_reference_flat_columns": dist_flat_columns,
        "dist_reference_categorical_values": categorical_values(dist_records),
        "current_training_dataset_schema": current_schema,
        "v5_training_dataset_schema": v5_schema,
        "v5_output_csv": str(v5_path),
        "v5_export_summary": export_summary,
        "model_feature_columns": list(FEATURE_COLUMNS),
        "missing_from_training_dataset": missing_current,
        "missing_from_v5_dataset": missing_v5,
        "extra_training_only_columns": sorted(field for field in current_fieldnames if field not in dist_flat_columns),
        "incompatible_types": incompatible_types(dist_schema, v5_schema),
        "fields_present_but_empty": fields_present_but_empty(v5_schema),
        "fields_used_by_model": list(FEATURE_COLUMNS),
        "fields_ignored_by_model": ignored_by_model,
        "leakage_risk_columns": {
            "present": leakage_columns,
            "used_by_model": leakage_used,
            "status": "ok" if not leakage_used else "leakage_features_used",
        },
        "documented_missing_dist_fields": documented_empty_fields(v5_schema, v5_fieldnames),
        "alignment_status": "aligned" if not missing_v5 and not leakage_used else "needs_review",
        "not_gto": True,
        "not_for_production": True,
        "bot_live_connection": "not_modified",
    }

    report_path.parent.mkdir(parents=True, exist_ok=True)
    report_path.write_text(json.dumps(report, ensure_ascii=False, indent=2, sort_keys=True), encoding="utf-8")
    return report


def load_dist_references(dist_dir: Path) -> list[tuple[Path, dict[str, Any]]]:
    references: list[tuple[Path, dict[str, Any]]] = []
    for path in sorted(dist_dir.glob("*.jsonl")):
        with path.open("r", encoding="utf-8-sig") as handle:
            for line in handle:
                text = line.strip()
                if not text:
                    continue
                value = json.loads(text)
                if isinstance(value, dict):
                    references.append((path, value))
    return references


def load_csv_rows(path: Path) -> tuple[list[dict[str, str]], list[str]]:
    with path.open("r", encoding="utf-8", newline="") as handle:
        reader = csv.DictReader(handle)
        rows = [dict(row) for row in reader]
        return rows, list(reader.fieldnames or [])


def infer_json_schema(records: Sequence[Mapping[str, Any]]) -> dict[str, Any]:
    total = len(records)
    return {
        "record_count": total,
        "fields": _infer_object_schema(records, total),
    }


def _infer_object_schema(objects: Sequence[Mapping[str, Any]], total: int) -> dict[str, Any]:
    values_by_key: dict[str, list[Any]] = defaultdict(list)
    for item in objects:
        for key, value in item.items():
            values_by_key[key].append(value)
    fields: dict[str, Any] = {}
    for key, values in sorted(values_by_key.items()):
        non_empty = [value for value in values if not is_empty(value)]
        field = {
            "present_count": len(values),
            "required": len(values) == total,
            "types": sorted({type_name(value) for value in non_empty} or {"empty"}),
            "empty_count": len(values) - len(non_empty),
        }
        dict_values = [value for value in non_empty if isinstance(value, Mapping)]
        if dict_values:
            field["fields"] = _infer_object_schema(dict_values, len(dict_values))
        else:
            cats = categorical_from_values(non_empty)
            if cats is not None:
                field["categorical_values"] = cats
        fields[key] = field
    return fields


def infer_csv_schema(rows: Sequence[Mapping[str, str]], fieldnames: Sequence[str]) -> dict[str, Any]:
    fields: dict[str, Any] = {}
    for field in fieldnames:
        values = [row.get(field, "") for row in rows]
        non_empty = [value for value in values if not is_empty(value)]
        fields[field] = {
            "present_count": len(values),
            "required": all(not is_empty(value) for value in values) if values else False,
            "types": sorted({csv_type_name(value) for value in non_empty} or {"empty"}),
            "empty_count": len(values) - len(non_empty),
            "categorical_values": categorical_from_values(non_empty),
        }
    return {"record_count": len(rows), "fields": fields}


def flatten_schema_paths(schema: Mapping[str, Any]) -> set[str]:
    paths: set[str] = set()

    def walk(fields: Mapping[str, Any], prefix: str = "") -> None:
        for key, info in fields.items():
            path = f"{prefix}.{key}" if prefix else key
            nested = info.get("fields") if isinstance(info, Mapping) else None
            if isinstance(nested, Mapping):
                walk(nested, path)
            else:
                paths.add(path)

    walk(schema.get("fields", {}))
    return paths


def incompatible_types(dist_schema: Mapping[str, Any], csv_schema: Mapping[str, Any]) -> dict[str, dict[str, list[str]]]:
    dist_types = flat_type_map(dist_schema)
    csv_types = {
        field: set(info.get("types", []))
        for field, info in dict(csv_schema.get("fields", {})).items()
    }
    incompatible: dict[str, dict[str, list[str]]] = {}
    for field, expected in sorted(dist_types.items()):
        actual = csv_types.get(field)
        if not actual:
            continue
        if types_compatible(expected, actual):
            continue
        incompatible[field] = {
            "dist_types": sorted(expected),
            "training_types": sorted(actual),
        }
    return incompatible


def flat_type_map(schema: Mapping[str, Any]) -> dict[str, set[str]]:
    result: dict[str, set[str]] = {}

    def walk(fields: Mapping[str, Any], prefix: str = "") -> None:
        for key, info in fields.items():
            path = f"{prefix}.{key}" if prefix else key
            nested = info.get("fields") if isinstance(info, Mapping) else None
            if isinstance(nested, Mapping):
                walk(nested, path)
            else:
                result[path] = set(info.get("types", []))

    walk(schema.get("fields", {}))
    return result


def types_compatible(expected: set[str], actual: set[str]) -> bool:
    if actual == {"empty"}:
        return True
    normalized_expected = normalize_type_set(expected)
    normalized_actual = normalize_type_set(actual)
    return bool(normalized_expected & normalized_actual)


def normalize_type_set(types: set[str]) -> set[str]:
    normalized = set(types)
    if normalized & {"int", "float"}:
        normalized.add("number")
    if normalized & {"json_array", "array"}:
        normalized.add("array")
    if normalized & {"json_object", "object"}:
        normalized.add("object")
    return normalized


def fields_present_but_empty(schema: Mapping[str, Any]) -> list[str]:
    count = int(schema.get("record_count", 0))
    return sorted(
        field for field, info in dict(schema.get("fields", {})).items()
        if count > 0 and info.get("empty_count") == count
    )


def documented_empty_fields(schema: Mapping[str, Any], fieldnames: Sequence[str]) -> dict[str, str]:
    empty = fields_present_but_empty(schema)
    return {
        field: DOCUMENTED_OPTIONAL_DIST_FIELDS.get(field, "present as empty because this offline bootstrap source cannot observe it")
        for field in empty
        if field in fieldnames
    }


def leakage_risk_columns(fieldnames: Sequence[str]) -> list[str]:
    return sorted(
        field for field in fieldnames
        if any(pattern in field for pattern in LEAKAGE_PATTERNS)
    )


def categorical_values(records: Sequence[Mapping[str, Any]]) -> dict[str, list[str]]:
    values: dict[str, Counter[str]] = defaultdict(Counter)
    for record in records:
        for path, value in flatten_values(record).items():
            if isinstance(value, (str, bool)) and not is_empty(value):
                values[path][str(value)] += 1
    return {
        path: sorted(counter)
        for path, counter in sorted(values.items())
        if len(counter) <= 30
    }


def flatten_values(value: Any, prefix: str = "") -> dict[str, Any]:
    if isinstance(value, Mapping):
        result: dict[str, Any] = {}
        for key, child in value.items():
            child_prefix = f"{prefix}.{key}" if prefix else str(key)
            result.update(flatten_values(child, child_prefix))
        return result
    return {prefix: value}


def categorical_from_values(values: Sequence[Any]) -> list[str] | None:
    simple = [value for value in values if isinstance(value, (str, bool))]
    if len(simple) != len(values):
        return None
    unique = sorted({str(value) for value in simple})
    return unique if len(unique) <= 30 else None


def type_name(value: Any) -> str:
    if value is None:
        return "empty"
    if isinstance(value, bool):
        return "bool"
    if isinstance(value, int) and not isinstance(value, bool):
        return "int"
    if isinstance(value, float):
        return "float"
    if isinstance(value, str):
        return "string"
    if isinstance(value, list):
        return "array"
    if isinstance(value, Mapping):
        return "object"
    return type(value).__name__


def csv_type_name(value: str) -> str:
    if is_empty(value):
        return "empty"
    lowered = str(value).strip().lower()
    if lowered in {"true", "false"}:
        return "bool"
    try:
        json_value = json.loads(value)
    except (json.JSONDecodeError, TypeError):
        json_value = None
    if isinstance(json_value, list):
        return "json_array"
    if isinstance(json_value, dict):
        return "json_object"
    try:
        int(value)
        return "int"
    except (TypeError, ValueError):
        pass
    try:
        float(value)
        return "float"
    except (TypeError, ValueError):
        return "string"


def is_empty(value: Any) -> bool:
    if value is None:
        return True
    if isinstance(value, str):
        return value.strip().lower() in {"", "none", "null"}
    return False


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--dist-dir", default=str(DEFAULT_DIST_DIR))
    parser.add_argument("--current-csv", default=str(DEFAULT_CURRENT_CSV))
    parser.add_argument("--v5-output", default=str(DEFAULT_V5_CSV))
    parser.add_argument("--report-output", default=str(DEFAULT_REPORT))
    return parser


def main(argv: list[str] | None = None) -> int:
    args = build_parser().parse_args(argv)
    report = run_alignment(
        dist_dir=args.dist_dir,
        current_csv=args.current_csv,
        v5_output=args.v5_output,
        report_output=args.report_output,
    )
    print(json.dumps(report, ensure_ascii=False, indent=2, sort_keys=True))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
